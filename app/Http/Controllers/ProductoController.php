    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $productos = new Producto();
        $productos->retiro = $request->get('retiro');
        $productos->numeracion_ini = $request->get('numeracion_ini');
        $productos->numeracion_fin = $request->get('numeracion_fin');
        $productos->tamano = $request->get('tamano');
        $productos->papel_id = $request->get('papel_id');
        $productos->corte_id = $request->get('corte_id');
        $productos->tipo_impresion_id = $request->get('tipo_impresion_id');

        $productos->save();

        return redirect('/productos');
    }

